int	ft_atoi(char *str)
{
	int	i; 
	int	j;
	int	r;

	r = 1;
	i = 0; 
	j = 0;
	while (((str[i] == 32) || (str[i] >= 9 && str[i] <= 12)))
	{
		i++;
	}
	while ((str[i] == '+') || str[i] == '-')
	{
		if (str[i] == '-')
		{
			r = r * -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = (j * 10) + (str[i] - '0');
		i++;
	}
	return (r * j); 
}
